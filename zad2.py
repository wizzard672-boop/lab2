# -*- coding: utf-8 -*-
"""
Задача 2. Арифметическое выражение
Условие: значение выражения 3·4³⁸ + 2·4²³ + 4²⁰ + 3·4⁵ + 2·4⁴ + 1
записали в системе счисления с основанием 16.
Сколько значащих нулей содержится в этой записи?
"""

def solve_task2():
    print("=" * 50)
    print("Задача 2. Арифметическое выражение")
    print("=" * 50)
    print()
    print("Условие:")
    print("  Выражение: 3·4³⁸ + 2·4²³ + 4²⁰ + 3·4⁵ + 2·4⁴ + 1")
    print("  Перевести в 16-ричную систему и посчитать значащие нули")
    print()
    
    # Вычисляем значение выражения
    # Используем оператор ** для возведения в степень
    value = 3 * 4**38 + 2 * 4**23 + 4**20 + 3 * 4**5 + 2 * 4**4 + 1
    
    print("Решение:")
    print(f"  1) Вычисляем значение выражения:")
    print(f"     - 3·4³⁸ = 3 × 4³⁸ = 3 × {4**38} = {3 * 4**38}")
    print(f"     - 2·4²³ = 2 × 4²³ = 2 × {4**23} = {2 * 4**23}")
    print(f"     - 4²⁰ = {4**20}")
    print(f"     - 3·4⁵ = 3 × 4⁵ = 3 × {4**5} = {3 * 4**5}")
    print(f"     - 2·4⁴ = 2 × 4⁴ = 2 × {4**4} = {2 * 4**4}")
    print(f"     - + 1")
    print(f"  2) Сумма: {value}")
    print()
    
    # Переводим в 16-ричную систему счисления
    # Функция hex() возвращает строку с префиксом '0x', убираем его срезом [2:]
    hex_value = hex(value)[2:]
    
    print(f"  3) Шестнадцатеричная запись: {hex_value}")
    print()
    
    # Подсчитываем количество нулей в шестнадцатеричной записи
    zero_count = hex_value.count('0')
    
    # Детальный разбор для понимания
    print("  4) Анализ записи:")
    print(f"     - Длина записи: {len(hex_value)} цифр")
    print(f"     - Символы в записи: {' '.join(hex_value)}")
    print(f"     - Позиции нулей: ", end="")
    zero_positions = []
    for i, char in enumerate(hex_value):
        if char == '0':
            zero_positions.append(str(i + 1))
    if zero_positions:
        print(f"позиции {', '.join(zero_positions)} (слева направо)")
    else:
        print("отсутствуют")
    print()
    
    print(f"Ответ: {zero_count} значащих нулей")
    print()
    
    return zero_count


if __name__ == "__main__":
    solve_task2()